package day_07;

public class Wrapper5 {
 public static void main(String[] args) {
	 Integer myInt = 5;
	 Double myDouble =9.6;
	 Character myCharacter = 'd';
	 
	 System.out.println(myInt.intValue());
	 System.out.println(myDouble.doubleValue());
	 System.out.println(myCharacter.charValue());
 }
}
