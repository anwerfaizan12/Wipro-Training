package day_04;


public class ValueOf {
	public static void main(String[] args) {
		  char ch='A';
		  float f=23.5f;
		  int i=65;
		  
		  String s1= String.valueOf(ch);
		  String s2 = String.valueOf(f);
		  String s3 = String.valueOf(i);
		  
		  System.out.println(s1);
		  System.out.println(s2);
		  System.out.println(s3);
	  }
}
