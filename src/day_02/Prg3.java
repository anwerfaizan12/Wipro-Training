package day_02;

public class Prg3 {
 public static void main(String[] args) {
	 
	 // Relational operators
	 int m=4;
	 int n=5;
	 int o=5;
	 if(m<n) {
		 System.out.println("true");
	 }
	 
	 if(n>m) {
		 System.out.println("false");
	 }
	 
	 if(m != n) {
		 System.out.println("Not equal");
	 }
	 
	 if(n==o) {
		 System.out.println("o and n are equal");
	 }
 }
}
