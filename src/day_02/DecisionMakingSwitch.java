package day_02;

public class DecisionMakingSwitch {
  public static void main(String[] args) {
	  int rating=3;
	  switch(rating) {
	  case 1:
		  System.out.println("Excellent");
		  break;
	  
  case 2:
	  System.out.println("Great");
	  break;
  
case 3:
	  System.out.println("Good");
	  break;

case 4:
	  System.out.println("Average");
	  break;
}
  }
}
